import re
print("贪婪模式-------")
rs=re.findall("python\d*","python2018")
print(rs)
rs=re.findall("python\d+","python2018")
print(rs)
rs=re.findall("python\d{2,}","python2018")
print(rs)
rs=re.findall("python\d{1,4}","python2018")
print(rs)
print("非贪婪模式---------")
rs=re.findall("python\d*?","python2018")
print(rs)
rs=re.findall("python\d+?","python2018")
print(rs)
rs=re.findall("python\d{2,}?","python2018")
print(rs)
rs=re.findall("python\d{1,4}?","python2018")
print(rs)
